//comparison operator are used to compare values of variables.
//comparison operator give output in boolean true and false.
let an=10;
console.log(an==10);//checking equals
console.log(an===6);//three(===)mean it checking the type and value.
console.log(an!=10); 
console.log(an>7);
console.log(an<7);
console.log(an>=2);
console.log(an<=10);


